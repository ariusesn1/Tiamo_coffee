    private void btnSerchHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSerchHDActionPerformed
        if (txtDate.getDate() == null) {
            return;
        }
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        modelTBHD.setRowCount(0);
        tongTien = 0;
        ArrayList<HoaDon> dshd = BLL.BLLHoaDon.Find(ChuyenDoi.LayNgayString(txtDate.getDate()));
        for (HoaDon hd : dshd) {
            Vector r = new Vector();
            r.add(hd.getMAHD());
            r.add(df.format(hd.getNGAYLAPHD()));
            r.add(BLL.BLLNhanVien.LayTenNV(hd.getMANV()));
            r.add(hd.getMABAN());
            r.add(hd.getGIAMGIA());
            r.add(hd.getTONGTIEN());
            tongTien += hd.getTONGTIEN();
            modelTBHD.addRow(r);
        }
        lbTongTien.setText("" + tongTien);
        lbThongBao.setText("KẾT QUẢ TÌM KIẾM:" + dshd.size());
    }//GEN-LAST:event_btnSerchHDActionPerformed

