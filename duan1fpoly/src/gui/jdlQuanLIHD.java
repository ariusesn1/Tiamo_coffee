    void loadData() {
        tongTien = 0;
        ds = BLL.BLLHoaDon.GetAll();
        modelTBHD.setRowCount(0);
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        for (HoaDon hd : ds) {
            Vector r = new Vector();
            r.add(hd.getMAHD());
            r.add(df.format(hd.getNGAYLAPHD()));
            r.add(hd.getMANV());
            r.add(hd.getMABAN());
            r.add(hd.getGIAMGIA());
            r.add(hd.getTONGTIEN());
            tongTien += hd.getTONGTIEN();
            modelTBHD.addRow(r);
        }
        lbTongTien.setText("" + tongTien);
    }
    
    void loadCTHD(String tukhoa) {
        modelTBCTHD.setRowCount(0);
        ArrayList<CTHD> dscthd = BLL.BLLCTHD.timKiem(tukhoa);
        for (CTHD ct : dscthd) {
            Vector r = new Vector();
            r.add(BLL.BLLThucDon.LayTenMon(ct.getMAMON()));
            r.add(ct.getDONGIA());
            r.add(ct.getSOLUONG());
            modelTBCTHD.addRow(r);
        }
    }

