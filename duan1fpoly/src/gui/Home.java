    //Tính tiền hóa đơn 
    void tinhTien() {
        float sum = 0;
        for (int i = 0; i < modeltblOrder.getRowCount(); i++) {
            sum += ChuyenDoi.SoDouble(modeltblOrder.getValueAt(i, 4).toString());
        }
        lbTongTien.setText("" + sum );
        float tienGiam = 0;
        
        if (!txtGiamGia.getText().equals("")) {
            float giamGia = (Float.parseFloat(txtGiamGia.getText()));
            tienGiam = sum * giamGia / (float) 100;
        }
        lbTienPhaiTra.setText("" + (sum - tienGiam));
    }
    
    //Tính tiền khách 
    void TinhTienKhach() {
        try {
            float conLai = Float.parseFloat(txtTienKhachDua.getText().trim()) - Float.parseFloat(lbTienPhaiTra.getText());
            if (conLai < 0) {
                lbStatus.setText("Khách đưa không đủ tiền");
                lbStatus.setForeground(Color.red);
                txtTienThua.setText("");
            } else {
                lbStatus.setText("");
                txtTienThua.setText("" + conLai);
            }
        } catch (Exception e) {
            
        }
    }
    
    private void tblBanHDMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBanHDMousePressed
            rowBan = tblBanHD.getSelectedRow();
            lbBan.setText(tblBanHD.getValueAt(rowBan, 0).toString());
            if (rowBan >= 0) {
                modeltblOrder.setRowCount(0);
                Vector dsMon = (Vector) dsMonTungBan.get(rowBan);
                for (int i = 0; i < dsMon.size(); i++) {
                    Vector r = (Vector) dsMon.get(i);
                    modeltblOrder.addRow(r);
                }
                tinhTien();
                TinhTienKhach();
            }
        
    }//GEN-LAST:event_tblBanHDMousePressed

    private void btnXoaMonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMonActionPerformed
        int row = tblOrder.getSelectedRow();
        rowBan = tblBanHD.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng nào để xoá", "Thông báo", 1);
            return;
        }

        if (JOptionPane.showConfirmDialog(null, "Bạn muốn xoá " + tblOrder.getValueAt(row, 1) + "?", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
            modeltblOrder.removeRow(row);
            if (tblOrder.getRowCount() == 0) {
               modeltblBan.setValueAt("Trống", rowBan, 3); 
            }
            ((Vector) dsMonTungBan.get(rowBan)).remove(row);
            tinhTien();
            TinhTienKhach();
        }
        
    }//GEN-LAST:event_btnXoaMonActionPerformed

