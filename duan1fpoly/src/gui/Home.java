    void LoadListBan(){
        BLL.BLLBan.updateTinhTrang();
        modeltblBan = (DefaultTableModel) tblBanHD.getModel();
        for (Ban b : BLL.BLLBan.GetAll()) {
            cbbBan.addItem(b.getTENBAN());
        }
        dsMonTungBan = new Vector();
        modeltblBan.setRowCount(0);
        for (Ban b : BLL.BLLBan.GetAll()) {
            Vector r = new Vector();
            r.add(b.getMABAN());
            r.add(b.getTENBAN());
            r.add(b.getLOAIBAN());
            r.add(b.getTRANGTHAI());
            modeltblBan.addRow(r);
            dsMonTungBan.add(new Vector());
        } 
    }
    

