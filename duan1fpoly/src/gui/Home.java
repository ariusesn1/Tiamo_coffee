    void themNuoc(int row) {
        boolean checkmon = true;
        int MonDuocChon = tblThucDon.getSelectedRow();
            if(MonDuocChon < 0){
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn món");
                return;
            }
            String input = JOptionPane.showInputDialog("Nhập số lượng mua");
            if(input.equals("")){
                JOptionPane.showMessageDialog(this, "Nhập số lượng cần mua");
            }else{
                
                tenmon = tblThucDon.getValueAt(MonDuocChon, 1).toString();
                int data = Integer.parseInt(input);
                int soluongmua = data;
                for (int i = 0; i < modeltblOrder.getRowCount(); i++) {
                    if (modeltblOrder.getValueAt(i, 1).toString().equals(tenmon)) {
                        int soluongbd = Integer.parseInt(modeltblOrder.getValueAt(i, 2).toString());
                        soluongbd += soluongmua;
                        modeltblOrder.setValueAt(soluongbd, i, 2);
                        modeltblOrder.setValueAt(soluongbd * Float.parseFloat(modeltblOrder.getValueAt(i, 3).toString()), i, 4);
                        checkmon = false;
                        break;
                    }
                }
                
                if (checkmon) {
                    Vector r = BLL.BLLThucDon.timTenNuocHD(tenmon, soluongmua);
                    modeltblOrder.addRow(r);
                }
                ((Vector) dsMonTungBan.get(row)).clear();
                for (int i = 0; i < modeltblOrder.getRowCount(); i++) {
                    Vector r = new Vector();
                    r.add(modeltblOrder.getValueAt(i, 0));
                    r.add(modeltblOrder.getValueAt(i, 1));
                    r.add(modeltblOrder.getValueAt(i, 2));
                    r.add(modeltblOrder.getValueAt(i, 3));
                    r.add(modeltblOrder.getValueAt(i, 4));
                    ((Vector) dsMonTungBan.get(row)).add(r);
                }
                modeltblBan.setValueAt("Có người", row, 3);
            }
    }
    
