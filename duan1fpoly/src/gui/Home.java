    private void btnChuyenBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenBanActionPerformed
        rowBan = tblBanHD.getSelectedRow();
        int index = cbbBan.getSelectedIndex();
        if (rowBan < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn bàn", "Thông báo", 1);
            return;
        }

        if (rowBan == index) {
            JOptionPane.showMessageDialog(null, "Khách đang ở " + tblBanHD.getValueAt(rowBan, 1).toString().toLowerCase() + " rồi nên không thể chuyển!", "Thông báo", 1);
            return;
        }
        
        if (JOptionPane.showConfirmDialog(null, "Bạn muốn chuyển từ " + modeltblBan.getValueAt(rowBan, 1).toString() + " sang " + modeltblBan.getValueAt(index, 1).toString(), "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
            dsMonTungBan.set(index, ((Vector) dsMonTungBan.get(rowBan)));
            modeltblOrder.setRowCount(0);
            dsMonTungBan.set(rowBan, new Vector());

            BLLBan.updateTinhTrang(tblBanHD.getValueAt(index, 0).toString(), true);
            BLLBan.updateTinhTrang(tblBanHD.getValueAt(rowBan, 0).toString(), false);
            JOptionPane.showMessageDialog(null, "Chuyển bàn thành công", "Thông báo", 1);
            tblBanHD.setValueAt("Trống", rowBan, 3);
            tblBanHD.setValueAt("Có người", index, 3);
            tblBanHDMousePressed(null);
        }
    }//GEN-LAST:event_btnChuyenBanActionPerformed

